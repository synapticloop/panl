package panl.response.panl.response;



public class Doc {
	public String mechanism_type;
	public String name;
	public long weight;
	public String brand;
	public String category;
	public String[] variants;
	public String[] images;
	public String grip_shape;
	
	/**
	 * <p>Return the mechanism_type.</p>
	 *
	 * @return the mechanism_type
	 */
	public String getMechanism_type() {
		return mechanism_type;
	}
	/**
	 * <p>Set the mechanism_type</p>
	 *
	 * @param mechanism_type the mechanism_type to set
	 */
	public void setMechanism_type(
			String mechanism_type) {
		this.mechanism_type = mechanism_type;
	}
	/**
	 * <p>Return the name.</p>
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * <p>Set the name</p>
	 *
	 * @param name the name to set
	 */
	public void setName(
			String name) {
		this.name = name;
	}
	/**
	 * <p>Return the weight.</p>
	 *
	 * @return the weight
	 */
	public long getWeight() {
		return weight;
	}
	/**
	 * <p>Set the weight</p>
	 *
	 * @param weight the weight to set
	 */
	public void setWeight(
			long weight) {
		this.weight = weight;
	}
	/**
	 * <p>Return the brand.</p>
	 *
	 * @return the brand
	 */
	public String getBrand() {
		return brand;
	}
	/**
	 * <p>Set the brand</p>
	 *
	 * @param brand the brand to set
	 */
	public void setBrand(
			String brand) {
		this.brand = brand;
	}
	/**
	 * <p>Return the category.</p>
	 *
	 * @return the category
	 */
	public String getCategory() {
		return category;
	}
	/**
	 * <p>Set the category</p>
	 *
	 * @param category the category to set
	 */
	public void setCategory(
			String category) {
		this.category = category;
	}
	/**
	 * <p>Return the variants.</p>
	 *
	 * @return the variants
	 */
	public String[] getVariants() {
		return variants;
	}
	/**
	 * <p>Set the variants</p>
	 *
	 * @param variants the variants to set
	 */
	public void setVariants(
			String[] variants) {
		this.variants = variants;
	}
	/**
	 * <p>Return the images.</p>
	 *
	 * @return the images
	 */
	public String[] getImages() {
		return images;
	}
	/**
	 * <p>Set the images</p>
	 *
	 * @param images the images to set
	 */
	public void setImages(
			String[] images) {
		this.images = images;
	}
	/**
	 * <p>Return the grip_shape.</p>
	 *
	 * @return the grip_shape
	 */
	public String getGrip_shape() {
		return grip_shape;
	}
	/**
	 * <p>Set the grip_shape</p>
	 *
	 * @param grip_shape the grip_shape to set
	 */
	public void setGrip_shape(
			String grip_shape) {
		this.grip_shape = grip_shape;
	}
}

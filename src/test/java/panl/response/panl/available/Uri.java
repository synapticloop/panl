package panl.response.panl.available;

public class Uri {
	public String before; // 	"/weighing+from+"
	public String before_min_value; // 	"/from+light"
	public boolean has_infix; // 	true
	public String after_max_value; // 	"heavy+pencils/w-w/"
	public String during; // 	"+to+"
	public String after; // 	"+grams/w-w/"
	/**
	 * <p>Return the before.</p>
	 *
	 * @return the before
	 */
	public String getBefore() {
		return before;
	}
	/**
	 * <p>Set the before</p>
	 *
	 * @param before the before to set
	 */
	public void setBefore(
			String before) {
		this.before = before;
	}
	/**
	 * <p>Return the before_min_value.</p>
	 *
	 * @return the before_min_value
	 */
	public String getBefore_min_value() {
		return before_min_value;
	}
	/**
	 * <p>Set the before_min_value</p>
	 *
	 * @param before_min_value the before_min_value to set
	 */
	public void setBefore_min_value(
			String before_min_value) {
		this.before_min_value = before_min_value;
	}
	/**
	 * <p>Return the has_infix.</p>
	 *
	 * @return the has_infix
	 */
	public boolean isHas_infix() {
		return has_infix;
	}
	/**
	 * <p>Set the has_infix</p>
	 *
	 * @param has_infix the has_infix to set
	 */
	public void setHas_infix(
			boolean has_infix) {
		this.has_infix = has_infix;
	}
	/**
	 * <p>Return the after_max_value.</p>
	 *
	 * @return the after_max_value
	 */
	public String getAfter_max_value() {
		return after_max_value;
	}
	/**
	 * <p>Set the after_max_value</p>
	 *
	 * @param after_max_value the after_max_value to set
	 */
	public void setAfter_max_value(
			String after_max_value) {
		this.after_max_value = after_max_value;
	}
	/**
	 * <p>Return the during.</p>
	 *
	 * @return the during
	 */
	public String getDuring() {
		return during;
	}
	/**
	 * <p>Set the during</p>
	 *
	 * @param during the during to set
	 */
	public void setDuring(
			String during) {
		this.during = during;
	}
	/**
	 * <p>Return the after.</p>
	 *
	 * @return the after
	 */
	public String getAfter() {
		return after;
	}
	/**
	 * <p>Set the after</p>
	 *
	 * @param after the after to set
	 */
	public void setAfter(
			String after) {
		this.after = after;
	}
	
	
}

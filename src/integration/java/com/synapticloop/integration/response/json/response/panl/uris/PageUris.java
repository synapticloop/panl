package com.synapticloop.integration.response.json.response.panl.uris;

public class PageUris {
	public String next;
	public String previous;
	public String before;
	public String after;
	/**
	 * <p>Return the next.</p>
	 *
	 * @return the next
	 */
	public String getNext() {
		return next;
	}
	/**
	 * <p>Set the next</p>
	 *
	 * @param next the next to set
	 */
	public void setNext(
			String next) {
		this.next = next;
	}
	/**
	 * <p>Return the previous.</p>
	 *
	 * @return the previous
	 */
	public String getPrevious() {
		return previous;
	}
	/**
	 * <p>Set the previous</p>
	 *
	 * @param previous the previous to set
	 */
	public void setPrevious(
			String previous) {
		this.previous = previous;
	}
	/**
	 * <p>Return the before.</p>
	 *
	 * @return the before
	 */
	public String getBefore() {
		return before;
	}
	/**
	 * <p>Set the before</p>
	 *
	 * @param before the before to set
	 */
	public void setBefore(
			String before) {
		this.before = before;
	}
	/**
	 * <p>Return the after.</p>
	 *
	 * @return the after
	 */
	public String getAfter() {
		return after;
	}
	/**
	 * <p>Set the after</p>
	 *
	 * @param after the after to set
	 */
	public void setAfter(
			String after) {
		this.after = after;
	}
	
	
}
